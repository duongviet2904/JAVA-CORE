
public class DiemTheoMonHoc {
	private String maSV;
	private String tenSV;
	private float diem;
	
	
	public DiemTheoMonHoc(String maSV, String tenSV, float diem) {
		this.maSV = maSV;
		this.tenSV = tenSV;
		this.diem = diem;
	}
	public String getMaSV() {
		return maSV;
	}
	public void setMaSV(String maSV) {
		this.maSV = maSV;
	}
	public String getTenSV() {
		return tenSV;
	}
	public void setTenSV(String tenSV) {
		this.tenSV = tenSV;
	}
	public float getDiem() {
		return diem;
	}
	public void setDiem(float diem) {
		this.diem = diem;
	}
}
